import 'package:flutter/material.dart';
import 'package:fyp_project_1/main.dart';
import 'package:fyp_project_1/screens/sign_up.dart';
import 'package:fyp_project_1/screens/forget_password.dart'; // Import the forget password screen
import 'package:icons_plus/icons_plus.dart';
import '../theme/theme.dart';
import '../widgets/custom_scaffold.dart';

class SignIn extends StatefulWidget {
  const SignIn({Key? key}) : super(key: key);

  @override
  State<SignIn> createState() => _SignInState();
}

class _SignInState extends State<SignIn> {
  final _formSignInKey = GlobalKey<FormState>();
  bool rememberPassword = true;

  @override
  Widget build(BuildContext context) {
    return CustomScaffold(
      child: Column(
        children: [
          const Expanded(
            flex: 1,
            child: SizedBox(
              height: 10,
            ),
          ),
          Expanded(
            flex: 7,
            child: Container(
              padding: const EdgeInsets.fromLTRB(25.0, 50.0, 25.0, 20.0),
              decoration: const BoxDecoration(
                color: Colors.white,
                borderRadius: BorderRadius.only(
                  topLeft: Radius.circular(40.0),
                  topRight: Radius.circular(40.0),
                ),
              ),
              child: SingleChildScrollView(
                child: Form(
                  key: _formSignInKey,
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.center,
                    children: [
                      Image.asset(
                        'assets/images/FanX1.png', // Adjust image path
                        height: 150, // Adjust height as needed
                        width: 150, // Adjust width as needed
